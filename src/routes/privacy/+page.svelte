    <section>
      <p class="lede">
        Hyllah is built to be a quiet, private home for your music collection. We take
        the privacy of your data seriously, both because we care and because we're legally
        required to under Norwegian and EU law (GDPR).
      </p>
      <p class="lede">
        This page explains exactly what we collect, why, how long we keep it, and what your
        rights are. No legalese, just plain English.
      </p>
    </section>

    <section>
      <h2>Who we are</h2>
      <p>
        Hyllah is operated by Frederik Flakne, an individual based in Norway.
        Contact: <a href="mailto:privacy@hyllah.com">privacy@hyllah.com</a>
      </p>
      <p>
        Under GDPR, we are the <strong>data controller</strong> for personal information you
        provide when you sign up and use the service.
      </p>
    </section>

    <section>
      <h2>What we collect</h2>

      <h3>Account data</h3>
      <p>When you sign up, we store:</p>
      <ul>
        <li>Your email address (required, used for magic-link login)</li>
        <li>Your username, display name, bio, and avatar (optional, only if you provide them)</li>
        <li>Your account creation date and last sign-in time</li>
      </ul>

      <h3>Collection data</h3>
      <p>Anything you add about your music collection, including:</p>
      <ul>
        <li>Record metadata (artist, title, year, label, format, condition)</li>
        <li>Personal notes, tags, and custom fields</li>
        <li>Purchase prices and personal valuations</li>
        <li>Tracklists and cover art images you upload</li>
      </ul>
      <p>
        <strong>All of this is private by default.</strong> Nothing in your collection is visible
        to anyone else unless you explicitly opt to make your profile public.
      </p>

      <h3>Technical data</h3>
      <ul>
        <li>
          IP address (used briefly for security — preventing abuse — then discarded; we do not
          log IPs persistently)
        </li>
        <li>
          Anonymous page-visit statistics via Umami analytics (no cookies, no fingerprinting, no
          personal identifiers)
        </li>
        <li>Standard server logs (access times, error logs) — retained for 30 days for debugging</li>
      </ul>
    </section>

    <section>
      <h2>What we don't collect</h2>
      <ul>
        <li>Passwords — we use magic-link login, so we never store passwords</li>
        <li>Payment information — the service is free; if you tip via PayPal, that transaction is between you and PayPal</li>
        <li>Tracking cookies or advertising identifiers</li>
        <li>Location data beyond what your IP roughly suggests</li>
        <li>Anything from third-party platforms (Spotify, Discogs profile, etc.)</li>
      </ul>
    </section>

    <section>
      <h2>Where your data lives</h2>
      <p>
        All personal data is stored on servers in the European Union (Zürich, Germany),
        operated by Supabase Inc. as our data processor. Cover-art images are stored in the
        same region. We do not transfer data outside the EEA.
      </p>
      <p>
        Email is sent through Resend, also EU-hosted. Analytics run on Umami's EU cloud.
        The website itself is served via Cloudflare's global CDN, but no personal data is
        cached there.
      </p>
    </section>

    <section>
      <h2>How long we keep your data</h2>
      <ul>
        <li><strong>Your account and collection:</strong> as long as your account exists</li>
        <li><strong>Server access logs:</strong> 30 days</li>
        <li><strong>Anonymous analytics:</strong> indefinitely (no personal data)</li>
        <li><strong>Deleted accounts:</strong> all personal data permanently erased within 30 days</li>
      </ul>
    </section>

    <section>
      <h2>Your rights under GDPR</h2>
      <p>You have the right to:</p>
      <ul>
        <li><strong>Access</strong> a copy of all personal data we hold about you</li>
        <li><strong>Correct</strong> any inaccurate data (most of this you can do yourself in your account settings)</li>
        <li><strong>Delete</strong> your account and all associated data ("right to be forgotten")</li>
        <li><strong>Export</strong> your collection data in a portable format (JSON)</li>
        <li><strong>Object</strong> to specific processing activities</li>
        <li><strong>Lodge a complaint</strong> with the Norwegian Data Protection Authority (Datatilsynet) at <a href="https://www.datatilsynet.no" target="_blank" rel="noopener">datatilsynet.no</a></li>
      </ul>
      <p>
        See the <a href="/gdpr">GDPR page</a> for instructions on how to exercise these rights,
        or just email <a href="mailto:privacy@hyllah.com">privacy@hyllah.com</a>.
      </p>
    </section>

    <section>
      <h2>Third parties we use</h2>
      <p>The following services help us operate Hyllah:</p>
      <ul>
        <li><strong>Supabase</strong> (EU) — database and authentication</li>
        <li><strong>Cloudflare</strong> (global CDN) — website hosting and DNS</li>
        <li><strong>Resend</strong> (EU) — sending login emails</li>
        <li><strong>Umami</strong> (EU) — anonymous analytics</li>
        <li><strong>PayPal</strong> (EU, optional) — only if you choose to use the tip jar</li>
      </ul>
      <p>
        Each of these has its own privacy policy and is a GDPR-compliant data processor.
        We do not share your data with any third party for marketing, advertising, or any
        purpose other than running the service.
      </p>
    </section>

    <section>
      <h2>Public profiles (optional)</h2>
      <p>
        If you choose to make your profile public, the following becomes visible at
        <code>hyllah.com/u/your-username</code>:
      </p>
      <ul>
        <li>Your username, display name, bio, and avatar (if you set them)</li>
        <li>Records and collections you mark as public</li>
        <li>Pricing information, only if you also enable that toggle</li>
      </ul>
      <p>
        Your email address is <strong>never</strong> shown publicly. You can revoke public
        visibility at any time in your account settings.
      </p>
    </section>

    <section>
      <h2>Cookies</h2>
      <p>
        We use only essential cookies required for authentication (your login session).
        We do not use tracking cookies, advertising cookies, or analytics cookies. This is
        why you don't see a cookie banner — there's nothing to consent to beyond the
        functional login cookie, which is exempt from consent requirements under EU law.
      </p>
    </section>

    <section>
      <h2>Children</h2>
      <p>
        Hyllah is not directed at children under 16. If you are under 16, please don't
        sign up without your parent or guardian's permission.
      </p>
    </section>

    <section>
      <h2>Changes to this policy</h2>
      <p>
        If we materially change how we handle your data, we'll update this page and notify
        active users by email. The "Last updated" date at the top reflects the most recent
        revision.
      </p>
    </section>

    <section>
      <h2>Contact</h2>
      <p>
        Questions, requests, complaints: <a href="mailto:privacy@hyllah.com">privacy@hyllah.com</a>
      </p>
    </section>
